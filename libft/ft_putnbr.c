#include "libft.h"

void	ft_putnbr(int n)
{
	long ln;

	ln = n;
	if (ln < 0)
	{
		ft_putchar('-');
		ln = ln * -1;
	}
	if (ln > 9)
	{
		ft_putnbr(ln / 10);
		ft_putnbr(ln % 10);
	}
	else
		ft_putchar(ln + '0');
}
