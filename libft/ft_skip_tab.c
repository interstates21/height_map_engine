#include <string.h>

int		ft_skip_tab(const char *str)
{
	int i;

	if (str == NULL)
		return (0);
	i = 0;
	while (str[i] == '\t' || str[i] == '\n' || str[i] == ' ' || str[i] == '\f'
		|| str[i] == '\r' || str[i] == '\v')
		i++;
	return (i);
}
