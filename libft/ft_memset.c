#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t	i;
	char	*str_s;

	i = 0;
	str_s = (char*)str;
	while (i < n)
	{
		str_s[i] = c;
		i++;
	}
	return (str);
}
